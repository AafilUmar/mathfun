package com.example.mathfun;

public class MathLib {


    public int sumOfTwo(int a,int b){
        return a+b;
    }
    public int sumOfThree(int a,int b,int c){
        return a+b+c;
    }
    public int productOfTwo(int a,int b){
        return a*b;
    }
    public int divOfTwo(int a,int b){
        return a/b;
    }
    public int subOfTwo(int a,int b){
        return a-b;
    }

}
